      SUBROUTINE MUMODEL2(THETA,MU_,ICALL,IDEF,NEWIND,&
      EVTREC,DATREC,IREV,NVNT,INDXS,F,G,H,IRGG,GG,NETAS)
      USE NMPRD4P
      USE SIZES,     ONLY: DPSIZE,ISIZE
      USE NMPRD_REAL,ONLY: ETA,EPS
      USE NMPRD_INT, ONLY: MSEC=>ISECDER,MFIRST=>IFRSTDER,COMACT,COMSAV
      USE NMPRD_INT, ONLY: IQUIT
      USE PRCM_INT,  ONLY: MC0000=>PRMC,ME0000=>PRME
      USE PRCM_INT,  ONLY: MG0000=>PRMG,MT0000=>PRMT
      USE NMBAYES_REAL, ONLY: LDF
      IMPLICIT REAL(KIND=DPSIZE) (A-Z)
      REAL(KIND=DPSIZE)   :: MU_(*)
      INTEGER NEWIND
      REAL(KIND=DPSIZE) :: EVTREC
      SAVE
      INTEGER(KIND=ISIZE) :: ICALL,IDEF,IREV,NVNT,INDXS,IRGG,NETAS
      DIMENSION :: IDEF(7,*),THETA(*),EVTREC(IREV,*),INDXS(*),GG(IRGG,71,*)
      IF (ICALL <= 1) THEN
      CALL ASSOCNMPRD4
      MC0000(1)=30
      ME0000(1)=70
      MG0000(1)=080
      MT0000(1)=70
      IDEF(1,001)= -9
      IDEF(1,002)= -1
      IDEF(1,003)=  0
      IDEF(1,004)=  0
      IDEF(2,003)=  0
      IDEF(2,004)=  0
      IDEF(3,002)=  7
      IDEF(4,001)=  6
      CALL GETETA(ETA)
      IF (IQUIT == 1) RETURN
      RETURN
      ENDIF
      IF (NEWIND /= 2) THEN
       IF (ICALL == 4) THEN
        CALL SIMETA(ETA)
       ELSE
        CALL GETETA(ETA)
       ENDIF
       IF (IQUIT == 1) RETURN
      ENDIF
      PROT=EVTREC(NVNT,03)
      AGE=EVTREC(NVNT,12)
      DOSE=EVTREC(NVNT,13)
      B00001=AGE-54.00D0
      B00002=1.D0+THETA(07)*B00001
      CLAGE=B00002
      CLCOV=CLAGE
      STRT=PROT*1000.D0+DOSE
      TVCL=DEXP(THETA(01))
      TVCL=CLCOV*TVCL
      MU_1=DLOG(TVCL)
      MU_(001)=MU_1
      B00004=MU_1+ETA(01)
      B00005=DEXP(B00004)
      CL=B00005
!                      A00073 = DERIVATIVE OF CL W.R.T. ETA(01)
      A00073=B00005
      TVV2=DEXP(THETA(02))
      MU_2=DLOG(TVV2)
      MU_(002)=MU_2
      B00006=MU_2+ETA(02)
      B00007=DEXP(B00006)
      V2=B00007
!                      A00078 = DERIVATIVE OF V2 W.R.T. ETA(02)
      A00078=B00007
      TVKA=THETA(03)
      MU_3=DLOG(TVKA)
      MU_(003)=MU_3
      B00008=MU_3+ETA(03)
      B00009=DEXP(B00008)
      KA=B00009
!                      A00083 = DERIVATIVE OF KA W.R.T. ETA(03)
      A00083=B00009
      TVF1=THETA(04)
      F1=TVF1
      TVQ=DEXP(THETA(05))
      MU_4=DLOG(TVQ)
      MU_(004)=MU_4
      B00010=MU_4+ETA(04)
      B00011=DEXP(B00010)
      Q=B00011
!                      A00088 = DERIVATIVE OF Q W.R.T. ETA(04)
      A00088=B00011
      TVV3=DEXP(THETA(06))
      MU_5=DLOG(TVV3)
      MU_(005)=MU_5
       RETURN
      B00012=MU_5+ETA(05)
      B00013=DEXP(B00012)
      V3=B00013
!                      A00093 = DERIVATIVE OF V3 W.R.T. ETA(05)
      A00093=B00013
      S2=V2*624.709D0/1.D6
      B00014=624.709D0/1.D6
!                      A00096 = DERIVATIVE OF S2 W.R.T. ETA(02)
      A00096=B00014*A00078
      GG(01,1,1)=CL
      GG(01,02,1)=A00073
      GG(02,1,1)=V2
      GG(02,03,1)=A00078
      GG(03,1,1)=Q
      GG(03,05,1)=A00088
      GG(04,1,1)=V3
      GG(04,06,1)=A00093
      GG(05,1,1)=KA
      GG(05,04,1)=A00083
      GG(06,1,1)=F1
      GG(07,1,1)=S2
      GG(07,03,1)=A00096
      IF (MSEC == 1) THEN
!                      A00075 = DERIVATIVE OF A00073 W.R.T. ETA(01)
      A00075=B00005
!                      A00080 = DERIVATIVE OF A00078 W.R.T. ETA(02)
      A00080=B00007
!                      A00085 = DERIVATIVE OF A00083 W.R.T. ETA(03)
      A00085=B00009
!                      A00090 = DERIVATIVE OF A00088 W.R.T. ETA(04)
      A00090=B00011
!                      A00095 = DERIVATIVE OF A00093 W.R.T. ETA(05)
      A00095=B00013
!                      A00097 = DERIVATIVE OF A00096 W.R.T. ETA(02)
      A00097=B00014*A00080
      GG(01,02,02)=A00075
      GG(02,03,03)=A00080
      GG(03,05,05)=A00090
      GG(04,06,06)=A00095
      GG(05,04,04)=A00085
      GG(07,03,03)=A00097
      ENDIF
      RETURN
      END
